    public boolean deletenhanvien(String manv) {
        String sql = "DELETE dbo.nhanvien WHERE manv = ?";
        try {
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, manv);
            int ii = ps.executeUpdate();
            if (ii > 0) {
                return true;
            }
        } catch (Exception e) {
            System.out.println("Lỗi " + e);
        }
        return false;
    }

    public boolean deletetaikhoan(String manv) {
        String sql1 = "	delete taikhan where manv = ?";
        try {
            PreparedStatement ps = con.prepareStatement(sql1);
            ps.setString(1, manv);
            int resulset = ps.executeUpdate();
            if (resulset > 0) {
                JOptionPane.showMessageDialog(this, "xóa thành công nhân viên ", "thông báo ", JOptionPane.PLAIN_MESSAGE);
            }
            return true;
        } catch (Exception e) {
            System.out.println("Lỗi xóa user" + e);

        }
        return false;
    }

