        int i = TableList.getSelectedRow();
        if (i >= 0) {
            txtmanv.setText((String) TableList.getValueAt(i, 1));
            txtfullname.setText((String) TableList.getValueAt(i, 2));
            txtngaysinh.setText((String) TableList.getValueAt(i, 3));
            txtdiachi.setText((String) TableList.getValueAt(i, 4));
            txtdantoc.setText((String) TableList.getValueAt(i, 5));
            if (TableList.getValueAt(i, 7) == "nam") {
                rdbNam.setSelected(true);
            } else {
                rdbNu.setSelected(true);
            }
//            if (TableList.getValueAt(i, 7) == "0") {
//                rdbNu.setSelected(true);
//            } else {
//                rdbNam.setSelected(true);
//            }
            for (PhongBan x : _Dao.getlstphongban()) {
                if (TableList.getValueAt(i, 9).equals(x.getMapb().toString().trim())) {
                    cboPhongban.setSelectedItem(x.getTenpb());
                }
            }
            for (ChucVu x : _Dao.getlstchucvu()) {
                if (TableList.getValueAt(i, 6).equals(x.getMacv().toString().trim())) {
                    cbochucvu.setSelectedItem(x.getTencv());
                }
            }
            txtsdt.setText((String) TableList.getValueAt(i, 8));
            txtsalary.setText(TableList.getValueAt(i, 10) + "");
        }

