    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
         StringBuilder sb = new StringBuilder();
        String sql = "update nhanvien set hoten = ?,gioitinh = ? ,dantoc = ? ,sdt = ? , ngaysinh = ? ,diachi = ? where manv = ?";      
        if (checkrong(txtfullname.getText())) {
            sb.append("Họ tên  nhân viên không được để trống !\n");
        }
        if (checkrong(txtdantoc.getText())) {
            sb.append("dân tộc  nhân viên không được để trống !\n");
        }
        if (checkrong(txtsdt.getText())) {
            sb.append("Số điện thoại  nhân viên không được để trống !\n");
        }
      
        if (checkrong(txtngaysinh.getText())) {
            sb.append("Ngày sinh  nhân viên không được để trống !\n");
        }
        if (checkrong(txtdiachi.getText())) {
            sb.append("địa chỉ  nhân viên không được để trống !\n");
        }

        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb, "thông báo", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                PreparedStatement ps = con.prepareStatement(sql);
                ps.setString(1, txtfullname.getText());
                ps.setInt(2, rdbnam.isSelected() ? 1 : 0);
                ps.setString(3, txtdantoc.getText());
                ps.setString(4, txtsdt.getText());

;
                
                ps.setString(5, txtngaysinh.getText());
                ps.setString(6, txtdiachi.getText());
                ps.setString(7, txtmanv.getText());
                int i = ps.executeUpdate();
                if (i > 0) {
                    JOptionPane.showMessageDialog(this, "Cập nhập thành công !", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Thất bại", "thông báo", JOptionPane.WARNING_MESSAGE);
                }
            } catch (Exception e) {
                System.out.println("Lỗi " + e);
            }
        }
    }//GEN-LAST:event_btnupdateActionPerformed
