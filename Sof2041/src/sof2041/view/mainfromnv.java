    public void filltext() {
        for (NhanVien x : _Dao.getlstnhanvien()) {
            if (x.getManv().equals(li.manv)) {
                txtmanv.setText(x.getManv());
                txtsalary.setText(x.getLuong()+"");
                txtfullname.setText(x.getHoten());
                txtngaysinh.setText(x.getNgaysinh());
                if (x.getGioitinh() == 1) {
                    rdbnam.setSelected(true);
                } else {
                    rdbnu.setSelected(true);
                }
                txtdantoc.setText(x.getDantoc());
                txtsdt.setText(x.getSdt());
//            txtsalary.setText(x.getLuong());
                txtdiachi.setText(x.getDiachi());
                String chucvu = x.getMachucvu();
                String phongban = x.getMapb();
                for (ChucVu y : _Dao.getlstchucvu()) {
                    if (y.getMacv().equals(chucvu)) {
                        combochucvu.addElement(y.getTencv());
                    }
                }
                for (PhongBan z : _Dao.getlstphongban()) {
                    if (z.getMapb().equals(phongban)) {
                        combophongban.addElement(z.getTenpb());
                    }
                }
            }
        }

    }

    private void fillcombo() {
        for (ChucVu x : _Dao.getlstchucvu()) {
            cbochucvu.addItem(x.getTencv());
//            System.out.println("x" + x.toString().trim());
        }
        for (PhongBan x : _Dao.getlstphongban()) {
            cbophongban.addItem(x.getTenpb());
//            System.out.println("x" + x.toString().trim());
        }
    }

